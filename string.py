#string conversion
#string datatype to another datatype

#implict
a = "Welcome to VKC"
print(a)
print(type(a))

#explict
a = str("saibhavani")
print(a)
print(type(a))

#data type /variable annotation
a : str = "VKC"
print(a)
print(type(a))

#binary
a = str(0b1001)
print(a)
print(type(a))

#octal
a = str(0b1001)
print(a)
print(type(a))

#hexa
a = str(0o56)
print(a)
print(type(a))

#boolean
a = str(True)
print(a)
print(type(a))

#boolean conversation
a = bool(0)
print(a)
print(type(a))

#hexa conversion
a = hex(0x45)
print(a)
print(type(a))

#octal conversion
a = oct(0o35)
print(a)
print(type(a))

#binary conversion
a = bin(0b1001)
print(a)
print(type(a))
